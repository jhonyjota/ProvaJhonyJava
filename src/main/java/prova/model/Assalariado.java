package prova.model;

public class Assalariado {

	private Integer id_assalariado;
	private Float salario;
	
	
	public Integer getId_assalariado() {
		return id_assalariado;
	}
	public void setId_assalariado (Integer id_assalariado) {
		this.id_assalariado = id_assalariado;
	}
	
	public Float getSalario (){
		return salario;
	}
	public void setSalario (Float salario){
		this.salario = salario;
	}
}
